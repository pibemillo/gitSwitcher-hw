var day = 'Monday'

switch(day) {
	case 'Monday' :
		console.log('I like pears');
		break;
	case 'Tuesday':
		console.log('I like apples');
		break;	
	case 'Wednesday':
		console.log('I like apricots');
		break;	
	case 'Thursday':
		console.log('I like bananas');
		break;	
	case 'Friday':
		console.log('I like oranges');
		break;	
	case 'Saturday':
		console.log('I like everything');
		break;
	case 'Sunday':
		console.log('Sleeping');
		break;							
	default:
		console.log('What day is it?')	
}




